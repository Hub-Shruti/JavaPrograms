package ex_01_Java_Basic;
// ex_01_Java_Basic is an Identifier.
public class Lab010_Identifiers {
    // Lab010_Identifiers is an Identifier.
    public static void main(String[] args) {

        /*

        Identifiers are used to uniquely identify the programming elements like classes, methods, variables, etc.
        Identifier can contain Alphabets (A-Z or a-z), Digits (0-9), and two special symbols i.e. Dollar ($) and Underscore(_).
        First character of an identifier must be an Alphabet (A-Z or a-z) or Dollar ($) or Underscore (_).
        Identifier cannot start with a Digit.
        Identifier can start with an Underscore (_) but it is usually not preferred and will create problem while using println().
        System.out.println(_);  // This will throw an error during execution (java: underscore not allowed here).
        Keywords or Reserved words can't be used as Identifiers.

         */
        System.out.println("Identifiers !");

    }
}
